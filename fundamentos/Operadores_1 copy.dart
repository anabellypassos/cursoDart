void main() {
//Aritmétisos (Operadores binários / infix)

  int a = 7;
  int b = 3;

  int resultado = a + b;

  print(resultado);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);


  //Operadoees Lógicos
  bool ehFragil = true;
  bool ehCaro = false;

  print(ehFragil && ehCaro); // E
  print(ehFragil || ehCaro); // OU
  print(ehFragil ^ ehCaro); //Ou Exclusivo
  print(!ehFragil); // Não  //Únario / Prefixado
}
